package com.locationlabs.speedbump.utils;

import android.util.Log;

/**
 *
 */
public class LogUtil {

    public static void v(String log) {
        Log.v("SpeedBump", log);
    }

    public static void d(String log) {
        Log.d("SpeedBump", log);
    }

    public static void e(String log) {
        Log.e("SpeedBump", log);
    }

}
